public class MyInfo {
    public static void main(String[] args) {
        // ประกาศตัวแปร
        String name = "Phet";
        int age = 23;
        String hobby = "coding";

        // แสดงผลด้วยการเชื่อมข้อความด้วย +
        System.out.println("My name is " + name);
        System.out.println("I am " + age + " years old.");
        System.out.println("My hobby is " + hobby + ".");
    }
}
